// #region add [計算加總]
// function
/**
 * 計算加總
 * @param {int} n1
 * @param {int} n2
 * @return {int}
 */
function add(n1, n2) {
    console.log(n1 + n2); // 輸出
    return n1 + n2; // 回傳值
}
// #endregion

add(8, -4);

let result = add(10, 15);
console.log(result); // 輸出 回傳值 不一定要用籃子(result)存回傳值 可直接輸出
